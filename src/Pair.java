

public class Pair {
	
	public double f;
	public double s;
	public int t;
	public int cnt;
	
	public Pair() {
		
	}
	
	public Pair(double x, double y, int t) {
		this.f = x;
		this.s = y;
		this.t = t;
	}
	public Pair(double x, double y, int t, int cnt) {
		this.f = x;
		this.s = y;
		this.t = t;
		this.cnt = cnt;
	}
}
